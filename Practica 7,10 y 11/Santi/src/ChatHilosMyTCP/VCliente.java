    DefaultListModel din= new DefaultListModel();
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jList1.setModel(din);
        try{
            int port= Integer.parseInt(jTextField1.getText());
            String url=jTextField2.getText();
            String nick= jTextField4.getText();
            if(cliente ==null){
                cliente = new MCliente (port,url,nick,this);
                cliente.start();
            }
        jButton1.setEnabled(false);
        jButton2.setEnabled(true);
        din.removeAllElements();
        jTextArea1.setText("");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Error de entrada de datos");
            cliente=null;
        }
    }//GEN-LAST:event_jButton1ActionPerformed
